import java.util.*;
public class  Wave {
    ArrayList<Creep> creepList;//size will be number of different creeps
    ArrayList<Integer> numberOfCreeps;
    int waveNumber;
public Wave(ArrayList<Creep> cList, ArrayList<Integer> numberList, int waveno){
       creepList=cList;
numberOfCreeps = numberList;
 waveNumber=waveno;
    
}
    
    public ArrayList<Creep> getCreepList(){
        return creepList;
    }
    public ArrayList<Integer> getNumberList(){
        return numberOfCreeps;
    }
    public int getWaveNo(){
        return waveNumber;
    }

}